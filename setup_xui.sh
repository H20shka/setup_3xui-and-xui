#!/bin/bash

# –°–∫—Ä–∏–ø—Ç –¥–ª—è –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–π —É—Å—Ç–∞–Ω–æ–≤–∫–∏ x-ui –ø–∞–Ω–µ–ª–∏ –Ω–∞ Ubuntu/Debian —Å–µ—Ä–≤–µ—Ä
# –ê–≤—Ç–æ—Ä: AI Assistant
# –î–∞—Ç–∞: 03.02.2026
# –ó–∞–ø—É—Å–∫–∞—Ç—å —Å –ø—Ä–∞–≤–∞–º–∏ root –∏–ª–∏ —á–µ—Ä–µ–∑ sudo

# –í–∫–ª—é—á–µ–Ω–∏–µ —Å—Ç—Ä–æ–≥–æ–≥–æ —Ä–µ–∂–∏–º–∞: –≤—ã—Ö–æ–¥ –ø—Ä–∏ –æ—à–∏–±–∫–µ, –≤—ã–≤–æ–¥ –∫–æ–º–∞–Ω–¥
set -e
set -x

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ –æ—à–∏–±–æ–∫
error_exit() {
    echo "–û—à–∏–±–∫–∞: $1"
    exit 1
}

# –ü—Ä–æ–≤–µ—Ä–∫–∞, –∑–∞–ø—É—â–µ–Ω –ª–∏ —Å–∫—Ä–∏–ø—Ç —Å sudo
if [ "$EUID" -ne 0 ]; then
    error_exit "–ó–∞–ø—É—Å—Ç–∏—Ç–µ —Å–∫—Ä–∏–ø—Ç —Å –ø—Ä–∞–≤–∞–º–∏ root (sudo ./setup_xui.sh)"
fi

echo "=== –ù–∞—á–∞–ª–æ —É—Å—Ç–∞–Ω–æ–≤–∫–∏ x-ui ==="

# –®–∞–≥ 1: –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –ø–∞–∫–µ—Ç–æ–≤
echo "–®–∞–≥ 1: –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å–ø–∏—Å–∫–∞ –ø–∞–∫–µ—Ç–æ–≤..."
apt update || error_exit "–ù–µ —É–¥–∞–ª–æ—Å—å –æ–±–Ω–æ–≤–∏—Ç—å –ø–∞–∫–µ—Ç—ã"

# –®–∞–≥ 2: –£—Å—Ç–∞–Ω–æ–≤–∫–∞ Docker –∏ Docker Compose
echo "–®–∞–≥ 2: –£—Å—Ç–∞–Ω–æ–≤–∫–∞ Docker –∏ Docker Compose..."
apt install -y docker.io docker-compose || error_exit "–ù–µ —É–¥–∞–ª–æ—Å—å —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å Docker"

# –®–∞–≥ 3: –ö–ª–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è x-ui
echo "–®–∞–≥ 3: –ö–ª–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è x-ui..."
if [ -d "x-ui" ]; then
    echo "–î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è x-ui —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç, —É–¥–∞–ª—è–µ–º..."
    rm -rf x-ui || error_exit "–ù–µ —É–¥–∞–ª–æ—Å—å —É–¥–∞–ª–∏—Ç—å —Å—É—â–µ—Å—Ç–≤—É—é—â—É—é –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é x-ui"
fi
git clone https://github.com/vaxilu/x-ui.git || error_exit "–ù–µ —É–¥–∞–ª–æ—Å—å –∫–ª–æ–Ω–∏—Ä–æ–≤–∞—Ç—å —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π"

# –®–∞–≥ 4: –ü–µ—Ä–µ—Ö–æ–¥ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é
echo "–®–∞–≥ 4: –ü–µ—Ä–µ—Ö–æ–¥ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é x-ui..."
cd x-ui || error_exit "–î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è x-ui –Ω–µ –Ω–∞–π–¥–µ–Ω–∞"

# –®–∞–≥ 5: –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ Docker
echo "–®–∞–≥ 5: –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ Docker..."
if [ -f "docker-compose.yml" ] || [ -f "docker-compose.yaml" ] || [ -f "compose.yml" ] || [ -f "compose.yaml" ]; then
    echo "–ù–∞–π–¥–µ–Ω —Ñ–∞–π–ª docker-compose, –∑–∞–ø—É—Å–∫–∞–µ–º..."
    docker-compose up -d || error_exit "–ù–µ —É–¥–∞–ª–æ—Å—å –∑–∞–ø—É—Å—Ç–∏—Ç—å x-ui —á–µ—Ä–µ–∑ docker-compose"
else
    echo "–§–∞–π–ª docker-compose.yml –Ω–µ –Ω–∞–π–¥–µ–Ω. –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ install.sh..."
    if [ -f "install.sh" ]; then
        echo "–ù–∞–π–¥–µ–Ω install.sh, –∑–∞–ø—É—Å–∫–∞–µ–º —É—Å—Ç–∞–Ω–æ–≤–∫—É..."
        bash install.sh || error_exit "–ù–µ —É–¥–∞–ª–æ—Å—å –≤—ã–ø–æ–ª–Ω–∏—Ç—å install.sh"
    else
        echo "‚ö†Ô∏è  Docker Compose —Ñ–∞–π–ª –Ω–µ –Ω–∞–π–¥–µ–Ω. –ü–æ–ø—ã—Ç–∫–∞ —Ä—É—á–Ω–æ–≥–æ –∑–∞–ø—É—Å–∫–∞ Docker..."
        # –†—É—á–Ω–æ–π –∑–∞–ø—É—Å–∫ x-ui —á–µ—Ä–µ–∑ Docker
        docker run -d \
          --name x-ui \
          -p 54321:54321 \
          -v $(pwd)/db/:/etc/x-ui/ \
          -v $(pwd)/cert/:/root/cert/ \
          --restart=unless-stopped \
          vaxilu/x-ui:latest || error_exit "–ù–µ —É–¥–∞–ª–æ—Å—å –∑–∞–ø—É—Å—Ç–∏—Ç—å x-ui —á–µ—Ä–µ–∑ Docker"
    fi
fi

# –®–∞–≥ 6: –ü–æ–ª—É—á–µ–Ω–∏–µ –ø–æ—Ä—Ç–∞ –∏–∑ –ª–æ–≥–æ–≤ Docker
echo "–®–∞–≥ 6: –ü–æ–ª—É—á–µ–Ω–∏–µ –ø–æ—Ä—Ç–∞ –ø–∞–Ω–µ–ª–∏ –∏–∑ –ª–æ–≥–æ–≤ Docker..."
PANEL_PORT=""

# –ñ–¥–µ–º –Ω–µ–º–Ω–æ–≥–æ, —á—Ç–æ–±—ã –ª–æ–≥–∏ –ø–æ—è–≤–∏–ª–∏—Å—å
sleep 10

# –ü–æ–ª—É—á–∞–µ–º –ª–æ–≥–∏ –∏ –∏—â–µ–º –ø–æ—Ä—Ç
logs=$(docker logs x-ui 2>&1)
PANEL_PORT=$(echo "$logs" | grep -oP 'start panel on \K\d+' || echo "")

if [ -z "$PANEL_PORT" ]; then
    echo "‚ö†Ô∏è  –ù–µ —É–¥–∞–ª–æ—Å—å –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –æ–ø—Ä–µ–¥–µ–ª–∏—Ç—å –ø–æ—Ä—Ç –∏–∑ –ª–æ–≥–æ–≤."
    echo "   –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –≤—Ä—É—á–Ω—É—é: docker logs x-ui"
    PANEL_PORT="port"
fi

# –®–∞–≥ 7: –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ IPv4-–∞–¥—Ä–µ—Å–∞ —Å–µ—Ä–≤–µ—Ä–∞
echo "–®–∞–≥ 7: –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –≤–Ω–µ—à–Ω–µ–≥–æ IPv4-–∞–¥—Ä–µ—Å–∞ —Å–µ—Ä–≤–µ—Ä–∞..."

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ IPv4
is_ipv4() {
    local ip=$1
    if [[ $ip =~ ^[0-9]+\.[0-9]+\.[0-9]+\.[0-9]+$ ]]; then
        return 0
    else
        return 1
    fi
}

# –ü–æ–ø—ã—Ç–∫–∞ –ø–æ–ª—É—á–∏—Ç—å –≤–Ω–µ—à–Ω–∏–π IPv4
SERVER_IP=""
for service in "ifconfig.me" "ipinfo.io/ip" "icanhazip.com"; do
    ip=$(curl -s --connect-timeout 5 "$service" 2>/dev/null)
    if [ -n "$ip" ] && is_ipv4 "$ip"; then
        SERVER_IP="$ip"
        break
    fi
done

# Fallback –Ω–∞ –ª–æ–∫–∞–ª—å–Ω—ã–π IPv4
if [ -z "$SERVER_IP" ]; then
    local_ips=$(hostname -I 2>/dev/null)
    for ip in $local_ips; do
        if is_ipv4 "$ip" && [ "$ip" != "127.0.0.1" ]; then
            SERVER_IP="$ip"
            echo "‚ö†Ô∏è  –ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –ª–æ–∫–∞–ª—å–Ω—ã–π IPv4: $SERVER_IP"
            break
        fi
    done
fi

if [ -z "$SERVER_IP" ]; then
    echo "‚ùå –ù–µ —É–¥–∞–ª–æ—Å—å –æ–ø—Ä–µ–¥–µ–ª–∏—Ç—å IPv4-–∞–¥—Ä–µ—Å —Å–µ—Ä–≤–µ—Ä–∞."
    echo "   –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ –∏–Ω—Ç–µ—Ä–Ω–µ—Ç—É –∏ –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ —Å–µ—Ç–∏."
    SERVER_IP="yourserverip"
fi

PANEL_URL="http://$SERVER_IP:$PANEL_PORT/panel/"

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç–∏ –ø–∞–Ω–µ–ª–∏ —Å –ø–æ–≤—Ç–æ—Ä–Ω—ã–º–∏ –ø–æ–ø—ã—Ç–∫–∞–º–∏
echo "–ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç–∏ –ø–∞–Ω–µ–ª–∏ x-ui..."
MAX_ATTEMPTS=5
ATTEMPT=1

while [ $ATTEMPT -le $MAX_ATTEMPTS ]; do
    echo "–ü–æ–ø—ã—Ç–∫–∞ $ATTEMPT/$MAX_ATTEMPTS..."
    if curl -s --max-time 10 --connect-timeout 5 "$PANEL_URL" > /dev/null 2>&1; then
        echo "‚úÖ –ü–∞–Ω–µ–ª—å x-ui –¥–æ—Å—Ç—É–ø–Ω–∞ –ø–æ –∞–¥—Ä–µ—Å—É: $PANEL_URL"
        break
    else
        if [ $ATTEMPT -eq $MAX_ATTEMPTS ]; then
            echo "‚ùå –ü–∞–Ω–µ–ª—å –Ω–µ –¥–æ—Å—Ç—É–ø–Ω–∞ –ø–æ—Å–ª–µ $MAX_ATTEMPTS –ø–æ–ø—ã—Ç–æ–∫."
            echo "   –ü—Ä–æ–≤–µ—Ä—å—Ç–µ:"
            echo "   - –ü—Ä–∞–≤–∏–ª—å–Ω–æ—Å—Ç—å IP: $SERVER_IP"
            echo "   - –ü—Ä–∞–≤–∏–ª—å–Ω–æ—Å—Ç—å –ø–æ—Ä—Ç–∞: $PANEL_PORT (–∏–∑ docker logs x-ui)"
            echo "   - –û—Ç–∫—Ä—ã—Ç –ª–∏ –ø–æ—Ä—Ç $PANEL_PORT –≤ firewall"
            echo "   - –ó–∞–ø—É—â–µ–Ω –ª–∏ Docker –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä: docker ps"
        else
            echo "‚è≥ –ü–∞–Ω–µ–ª—å –µ—â–µ –Ω–µ –≥–æ—Ç–æ–≤–∞, –∂–¥–µ–º 15 —Å–µ–∫—É–Ω–¥..."
            sleep 15
        fi
    fi
    ((ATTEMPT++))
done

# –®–∞–≥ 8: –ó–∞–≤–µ—Ä—à–µ–Ω–∏–µ
echo ""
echo "=== –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞ —É—Å–ø–µ—à–Ω–æ! ==="
echo ""
echo "üåê –î–ª—è –¥–æ—Å—Ç—É–ø–∞ –∫ X-UI –æ—Ç–∫—Ä–æ–π—Ç–µ –±—Ä–∞—É–∑–µ—Ä –∏ –ø–µ—Ä–µ–π–¥–∏—Ç–µ –ø–æ –∞–¥—Ä–µ—Å—É:"
echo "   $PANEL_URL"
echo ""
echo "üîë –î–∞–Ω–Ω—ã–µ –¥–ª—è –≤—Ö–æ–¥–∞:"
echo "   –õ–æ–≥–∏–Ω: admin"
echo "   –ü–∞—Ä–æ–ª—å: admin"
echo ""
echo "‚ö†Ô∏è  –í–∞–∂–Ω–æ: –°—Ä–∞–∑—É –∏–∑–º–µ–Ω–∏—Ç–µ –ø–∞—Ä–æ–ª—å –ø–æ—Å–ª–µ –ø–µ—Ä–≤–æ–≥–æ –≤—Ö–æ–¥–∞!"
echo ""
echo "üí° –ï—Å–ª–∏ IP –Ω–µ –ø—Ä–∞–≤–∏–ª—å–Ω—ã–π, –∑–∞–º–µ–Ω–∏—Ç–µ $SERVER_IP –Ω–∞ –≤–∞—à —Ä–µ–∞–ª—å–Ω—ã–π IP-–∞–¥—Ä–µ—Å —Å–µ—Ä–≤–µ—Ä–∞."
echo "üí° –ï—Å–ª–∏ –ø–æ—Ä—Ç –Ω–µ –æ–ø—Ä–µ–¥–µ–ª–∏–ª—Å—è, –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ –∫–æ–º–∞–Ω–¥—É: docker logs x-ui"